package advance.dev;

public class Person {
	String ten;
	int tuoi;
	String soDT;

	public Person(String ten, int tuoi, String soDT) {
		super();
		this.ten = ten;
		this.tuoi = tuoi;
		this.soDT = soDT;
	}

	public String getTen() {
		return ten;
	}

	public void setTen(String ten) {
		this.ten = ten;
	}

	public int getTuoi() {
		return tuoi;
	}

	public void setTuoi(int tuoi) {
		this.tuoi = tuoi;
	}

	public String getSoDT() {
		return soDT;
	}

	public void setSoDT(String soDT) {
		this.soDT = soDT;
	}

}
